package com.thinh.snaplet.ui.theme

object MotionTokens {

    /** Instant feedback (tap, press, ripple sync) */
    const val Instant = 50

    /** Very fast UI response (icon swap, small fade) */
    const val Fast = 100

    /** Default UI animation (fade, scale, crossfade) */
    const val Normal = 180

    /** Emphasized UI (overlay, modal appear) */
    const val Emphasized = 250

    /** Large element transition (sheet, panel) */
    const val Slow = 350

    /** Heavy / deliberate transition (rare) */
    const val VerySlow = 500
}
